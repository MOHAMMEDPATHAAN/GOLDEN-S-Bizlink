// Fallback to English for missing translations
const getTranslation = (lang: string, key: TranslationKey): string => {
  const langTranslations = translations[lang] || translations['en']
  return langTranslations[key] || translations['en'][key] || key
}

// Translation function
export const t = (key: TranslationKey, lang = 'en'): string => {
  return getTranslation(lang, key)
}

// Get language info
export const getLanguage = (code: string): Language | undefined => {
  return LANGUAGES.find(l => l.code === code)
}

// Check if language is RTL
export const isRTL = (code: string): boolean => {
  const lang = getLanguage(code)
  return lang?.rtl || false
}

// Apply language to document
export const applyLanguage = (code: string): void => {
  if (typeof window === 'undefined') return
  
  const lang = getLanguage(code)
  const html = document.documentElement
  
  html.setAttribute('lang', code)
  html.setAttribute('dir', lang?.rtl ? 'rtl' : 'ltr')
}

// Create i18n hook for React
export const createI18nHook = (language: string) => {
  return (key: TranslationKey): string => t(key, language)
}
